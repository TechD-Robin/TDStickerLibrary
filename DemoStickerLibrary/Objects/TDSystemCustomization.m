#pragma mark -
#pragma mark class TDSystemCustomization

//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------
#pragma mark declare private category ()
//  ------------------------------------------------------------------------------------------------
//  --------------------------------
@interface TDSystemCustomization ()
{
    UIImage                       * backToMenuImage;
}
//  ------------------------------------------------------------------------------------------------

@end


//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------


//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------
#pragma mark -
#pragma mark declare private category (Private)
//  ------------------------------------------------------------------------------------------------
@interface TDSystemCustomization (Private)

//  ------------------------------------------------------------------------------------------------
#pragma mark declare for initial this class.
//  ------------------------------------------------------------------------------------------------
/**
 *  @brief initial the attributes of class.
 *  initial the attributes of class.
 */
- ( void ) _InitAttributes;


//  ------------------------------------------------------------------------------------------------

//  ------------------------------------------------------------------------------------------------


@end


//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------

//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------
#pragma mark -
#pragma mark implementation private category (Private)
//  ------------------------------------------------------------------------------------------------
@implementation TDSystemCustomization (Private)

//  ------------------------------------------------------------------------------------------------
#pragma mark method for initial this class.
//  ------------------------------------------------------------------------------------------------
//  --------------------------------
- ( void ) _InitAttributes
{
    [self                           setBackgroundColor: [UIColor clearColor]];
    [self                           setNormalTinyColor: nil];
    
    //  for UIimage.
//    [self                           setBackToMenuImage: nil];
    backToMenuImage                 = nil;
//    [self                           setBackToMenuImageHighlighted: nil];
    
    backToMenuImageHighlighted      = nil;
    
}

//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------

@end

//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------


//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------
#pragma mark -
#pragma mark implementation for public
//  ------------------------------------------------------------------------------------------------
@implementation TDSystemCustomization

//  ------------------------------------------------------------------------------------------------
#pragma mark synthesize variable.
@synthesize backgroundColor                 = _backgroundColor;
@synthesize normalTinyColor                 = _normalTinyColor;

//  for UIimage.
//@synthesize backToMenuImage                 = _backToMenuImage;
//@synthesize backToMenuImageHighlighted      = _backToMenuImageHighlighted;


//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------
#pragma mark overwrite implementation of NSObject
//  ------------------------------------------------------------------------------------------------
- ( instancetype ) init
{
    self                            = [super init];
    if ( nil == self )
    {
        return nil;
    }
    
    [self                           _InitAttributes];
    
    return self;
}

//  --------------------------------
//  ------------------------------------------------------------------------------------------------
#pragma mark declare for create the object.
//  ------------------------------------------------------------------------------------------------
static TDSystemCustomization              * _customization = nil;

//  ------------------------------------------------------------------------------------------------
+ ( instancetype ) customization
{
    static dispatch_once_t          oneToken;
    
    dispatch_once( &oneToken, ^
    {
        _customization              = [[self alloc] init];
    });
    return _customization;
}

//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------
- ( void ) setBackToMenuImage:(UIImage *)image
{
    backToMenuImage                 = image;
}

//  ------------------------------------------------------------------------------------------------
- ( UIImage * ) backToMenuImage
{
    return backToMenuImage;
}


//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------



//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------

@end

